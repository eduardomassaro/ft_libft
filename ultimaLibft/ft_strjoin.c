#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*conc;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (s1 == 0 || s2 == 0)
		return (0);
	conc = malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(char*));
	if (conc == 0)
		return (0);
	while (s1[i] != '\0')
	{
		conc[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		conc[i] = s2[j];
		j++;
		i++;
	}
	conc[i] = '\0';
	return (conc);
}
